import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");
const sourcePath = process.env.SJRE_SOURCE_JSON || "/tmp/sjre-pages-full.json";
const publicAssetDir = path.join(root, "public", "assets", "live");
const outputPath = path.join(root, "src", "content", "generatedSiteData.ts");

const slugify = (value) =>
  value
    .toLowerCase()
    .replace(/%[0-9a-f]{2}/gi, " ")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "")
    .slice(0, 90);

const extensionFor = (url) => {
  const pathname = new URL(url).pathname;
  const ext = path.extname(pathname).toLowerCase();
  if ([".jpg", ".jpeg", ".png", ".webp", ".gif"].includes(ext)) return ext;
  return ".jpg";
};

const cleanHref = (href) => {
  if (!href) return "";
  try {
    const url = new URL(href);
    if (url.hostname === "southjerseyreal.estate") return url.pathname;
  } catch {
    return href;
  }
  return href;
};

const source = JSON.parse(await readFile(sourcePath, "utf8"));
const assetUrls = new Map();

for (const page of source.pages) {
  for (const section of page.pageSections ?? []) {
    for (const image of section.images ?? []) {
      if (!image.src) continue;
      const url = image.src.replace(/\?format=\d+w$/, "?format=1500w");
      if (!assetUrls.has(url)) {
        const baseName = decodeURIComponent(new URL(url).pathname.split("/").pop() || "asset");
        const fileName = `${slugify(baseName)}${extensionFor(url)}`;
        assetUrls.set(url, {
          original: url,
          publicPath: `/assets/live/${fileName}`,
          localPath: path.join(publicAssetDir, fileName),
        });
      }
    }
  }
}

await mkdir(publicAssetDir, { recursive: true });

for (const asset of assetUrls.values()) {
  try {
    const response = await fetch(asset.original);
    if (!response.ok) throw new Error(`${response.status} ${response.statusText}`);
    const bytes = Buffer.from(await response.arrayBuffer());
    await writeFile(asset.localPath, bytes);
  } catch (error) {
    console.warn(`Could not download ${asset.original}: ${error.message}`);
  }
}

const resolveAsset = (src) => {
  if (!src) return "";
  const normalized = src.replace(/\?format=\d+w$/, "?format=1500w");
  return assetUrls.get(normalized)?.publicPath || src;
};

const pages = source.pages.map((page) => ({
  path: page.path,
  title: page.title,
  sections: (page.pageSections ?? []).map((section) => ({
    id: section.id || "",
    blocks: (section.blocks ?? []).map((block) => ({
      tag: block.tag,
      text: block.text,
      href: cleanHref(block.href),
      type: block.type || "",
      placeholder: block.placeholder || "",
    })),
    images: (section.images ?? []).map((image) => ({
      src: resolveAsset(image.src),
      alt: image.alt || "",
    })),
  })),
}));

const output = `// Generated by scripts/import-live-content.mjs from the live Squarespace site.
// Edit text and section data here, or rerun npm run import:live after a fresh extraction.
import type { SitePage } from "./types";

export const generatedPages: SitePage[] = ${JSON.stringify(pages, null, 2)};
`;

await mkdir(path.dirname(outputPath), { recursive: true });
await writeFile(outputPath, output);

console.log(`Wrote ${pages.length} pages to ${path.relative(root, outputPath)}`);
console.log(`Downloaded ${assetUrls.size} image assets to ${path.relative(root, publicAssetDir)}`);
